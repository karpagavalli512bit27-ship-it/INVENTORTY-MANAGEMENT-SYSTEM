    private void btuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btuActionPerformed
        // TODO add your handling code here:
        String names = name.getText();
        String phones = phone.getText();
        String addrs = addr.getText();
        if (validateFields()) {
            JOptionPane.showConfirmDialog(null, "All fields are required");
        } else {
            try {
                Connection con = DBConnection.getConnection();
                PreparedStatement ps = con.prepareStatement("UPDATE customer SET  name = ?,phone=?,address=? where id=?");
                ps.setString(1, names);
                ps.setString(2, phones);
                ps.setString(3, addrs);
                ps.setInt(4, cust_id);
                ps.executeUpdate();
                JOptionPane.showConfirmDialog(null, "Customer updated successfully");
                setVisible(false);
                new Customer().setVisible(true);
            } catch (Exception e) {
                JOptionPane.showConfirmDialog(null, e);
            }
        }
    }//GEN-LAST:event_btuActionPerformed

