    private void btsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btsActionPerformed
        // TODO add your handling code here:
        String names = name.getText();
        String phones = phone.getText();
        String addrs = addr.getText();
        if (validateFields()) {
            JOptionPane.showConfirmDialog(null, "All fields are required");

        } else {
            try {
                Connection con = DBConnection.getConnection();
                PreparedStatement ps = con.prepareStatement("insert into customer ( name,phone,address) VALUES ( ?, ?, ?)");
                ps.setString(1, names);
                ps.setString(2, phones);
                ps.setString(3, addrs);
                ps.executeUpdate();
                JOptionPane.showConfirmDialog(null, "Customer added successfully");
                setVisible(false);
                new Customer().setVisible(true);
            } catch (Exception e) {
                JOptionPane.showConfirmDialog(null, e);
            }
        }
    }//GEN-LAST:event_btsActionPerformed

    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) ctable.getModel();
        try {
            Connection con = DBConnection.getConnection();
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select* from customer");
            while (rs.next()) {
                model.addRow(new Object[]{rs.getString("id"), rs.getString("name"), rs.getString("phone"), rs.getString("address"), rs.getString(4)});
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
        btu.setEnabled(false);
    }//GEN-LAST:event_formComponentShown

