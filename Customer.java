    private void ctableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_ctableMouseClicked
        // TODO add your handling code here:
        int index = ctable.getSelectedRow();
        TableModel model = ctable.getModel();
        String id = model.getValueAt(index, 0).toString();
        cust_id = Integer.parseInt(id);
        String names = model.getValueAt(index, 1).toString();
        name.setText(names);
        String phones = model.getValueAt(index, 2).toString();
        phone.setText(phones);
        String addrs = model.getValueAt(index, 3).toString();
        addr.setText(addrs);
        bts.setEnabled(false);
        btu.setEnabled(true);

    }//GEN-LAST:event_ctableMouseClicked

